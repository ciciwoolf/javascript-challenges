
let arrayX = ['red', 'green', 'blue'];

//push - add an element
arrayX.push("purple");
console.log(arrayX);

//pop - removes final elements
let removedElement = arrayX.pop()
console.log(arrayX)
console.log(removedElement)

//shift - removes first elements
let remEle = arrayX.shift()
console.log(arrayX)
console.log(remEle)

//unshift - addes to the beginning
arrayX.unshift("turqoise")
console.log(arrayX)